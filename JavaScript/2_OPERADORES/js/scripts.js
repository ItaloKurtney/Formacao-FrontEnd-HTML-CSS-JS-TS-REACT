// 1 - NUMBER

console.log(typeof 2);
console.log(typeof 2.6);
console.log(typeof -232);

// 2 - OPEREÇOES ARITMETICAS

console.log(10+2);
console.log(3*3);
console.log(4-2);
console.log(12/2);
console.log(10+3*3);

// 3 - SPECIAL NUMBERS

console.log(typeof Infinity);
console.log(typeof -Infinity);
console.log("text" * 10);
console.log(typeof NaN);

// 4 - STRINGS 

console.log("UM TEXTO");
console.log('UM TEXTO COM CRASES');

// 5 - SIMBOLOS ESPECIAIS EM STRINGS

console.log("ESSE TEXTO TEM \n DUAS LINHAS");
console.log("ESSE TEXTO POSSUI UM \t ESPAÇAMENTO");

// 6 - CONCATENAÇÃO 

console.log("MEU " + "TEXTO " + "ESTA CONCATENADO EM 3 PARTES");

// 7 - INTERPOLAÇÃO

console.log(`A SOMA DE 2 + 2 é: ${2 + 2} FEITO COM INTERPOLAÇÃO`);

// 8 - BOOLEANOS

console.log(true);
console.log(false);
console.log(`30 é maior que 20 ? ${30 >20}`);
console.log(`30 é menor que 20 ? ${30 <20}`);
console.log(typeof false);

// 9 - COMPARAÇÕES 

console.log(5 < 5);
console.log(`10 é igual a 10 ? ${10 == 10}`);
console.log(`10 é igual a 9 ? ${10 == 9}`);
console.log(` 10 é diferente que 9 ? ${ 10 != 9}`);

// 10 - COMPARAÇÃO DE IDÊNTICO

console.log(9 === "9");

// 11 - OPERADORES LÓGICOS

console.log(true && true);
console.log(true && false);
console.log(5 > 2 && 2 < 10);
console.log(5>2 && "Italo" === 1);
console.log(5>2 || "Italo" === 1);

// 12 - VALORES VALIOS

console.log(typeof null, typeof undefined);
console.log(null === undefined);
console.log(null == undefined);

//13 - MUDANÇAS DE TIPOS 

console.log(5 * null);
console.log("10" + 1);
console.log("10" - 1);





